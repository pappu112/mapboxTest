    public void toast(LocationEngineResult result){
        Toast.makeText(MainActivity.this,"New location"+String.valueOf(result.getLastLocation().getLatitude())+","+String.valueOf(result.getLastLocation().getLongitude()),Toast.LENGTH_SHORT).show();
    }

    void addDataToServer(String lat,String lng){
        String deviceName = DeviceName.getDeviceName();

        ApiService service = NetworkClient.getRetrofitClient().create(ApiService.class);
        Call<String> call;
        call = service.addData(deviceName,lat,lng);
        call.enqueue(new Callback<String>() {
            @Override
            public void onResponse(Call<String> call, Response<String> response) {
                Log.e("response",response.body());

            }

            @Override
            public void onFailure(Call<String> call, Throwable t) {
                Log.e("response",t.toString());

            }
        });

    }



